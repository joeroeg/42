# Basic Arithmetic
print(3 + 2) # Example of using addition
print(3 - 2) # Example of using subtraction
print(3 * 2) # Example of using multiplication
print(3 ** 2) # Example of using exponentiation
print(3 / 2) # Example of using division
print(3 // 2) # Example of using floor division
print(3 % 2) # Example of using modulus
print(3 + 2 * 2) # Example of using precedence
print((3 + 2) * 2) # Example of using precedence

# Increment and Decrement
num = 1
num += 1 # Example of using increment
num -= 1 # Example of using decrement
print(num)

# Absolute Value
print(abs(-3)) # Example of using absolute value

# Round
print(round(3.75)) # Example of using round
print(round(3.75, 1)) # Example of using round

# Type Conversion
print(bin(3)) # Example of using binary function
print(hex(3)) # Example of using hexadecimal function
print(oct(3)) # Example of using octal function
print(int('0b11', 2)) # Example of using integer function
print(float(3)) # Example of using float function
print(complex(3)) # Example of using complex function

# Built-in Math Functions
print(divmod(3, 2)) # Example of using divmod function
print(pow(3, 2)) # Example of using power function
print(pow(3, 2, 2)) # Example of using power function
print(sum([1, 2, 3])) # Example of using sum function
print(max([1, 2, 3])) # Example of using max function
print(min([1, 2, 3])) # Example of using min function

# Comparison Operators
print(3 == 2) # Example of using equal to
print(3 != 2) # Example of using not equal to
print(3 > 2) # Example of using greater than
print(3 < 2) # Example of using less than
print(3 >= 2) # Example of using greater than or equal to
print(3 <= 2) # Example of using less than or equal to

# Chaining Comparison Operators
num = 3
print(1 < num < 10) # Example of using chaining comparison operators
print(10 > num <= 9) # Example of using chaining comparison operators

# Strings
num = 3
num = '3'
print(type(num)) # Example of using
print(3 + 2) # Example of using
print(int(num) + 2) # Example of using
