int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	x;
	int	y;

	i = 0;
	x = 0;
	y = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s2[i] == '\0')
			s2[i + 1] = '\0';
		if (s1[i] == '\0')
			s1[i + 1] = '\0';
		x = s1[i] - s2[i];
		y = y + x;
		i++;
	}	
	return (y);
}
