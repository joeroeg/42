int	ft_atoi(char *str)
{
	int	i;
	int	neg;
	int	n;

	i = 0;
	n = 0;
	neg = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
	{
		i++;
	}
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		n = n * 10 + str[i] - 48;
		i++;
	}
	if (neg % 2 == 1)
		return (-n);
	return (n);
}
