int	ft_atoi(char *str)
{
	int	i;
	int	r;
	int	n;

	i = 0;
	r = 0;
	n = 1;
	while (str[i] >= 9 && str[i] <= 13 || str[i] == 32)
		i++;
	while (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			n = n * -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		r = (r * 10) + (str[i] - 48);
		i++;
	}
	return (r * n);
}
