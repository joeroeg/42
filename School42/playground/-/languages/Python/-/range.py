# Using range() to generate a sequence of numbers
for i in range(5):
    print(i)

# In this example, we're using the range() function to generate a sequence of numbers from 0 to 8 (inclusive), with a step value of 2. The for loop is used to iterate over each number in the sequence, and the print() function is used to display the number to the console.
# Using range() with a step value
for i in range(0, 10, 2):
    print(i)
