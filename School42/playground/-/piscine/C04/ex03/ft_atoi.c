int	ft_atoi(char *str)
{
	int	i;
	int	nbr;
	int	swap;

	nbr = 0;
	swap = 1;
	i = 0;
	while ((str[i] == ' ' || (str[i] >= '\t' && str[i] <= '\r')))
	i++;
	while (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			swap *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nbr = nbr * 10 + (str[i] - '0');
		i++;
	}
	return (nbr * swap);
}
