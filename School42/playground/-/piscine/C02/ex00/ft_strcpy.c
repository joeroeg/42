#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
int main()
{
	char destination[] = "";
	char source[] = "source text";
	ft_strcpy(destination, source);
	printf("%s\n", ft_strcpy(destination, source));
}