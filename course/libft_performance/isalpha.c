#include	<stdio.h>

int	isalpha(c)
	int c;
{
	return ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') ? 1 : 0);
}

int	main(void)
{
	char	c;

	c = '1';
	printf("%d", isalpha(c));
	return (0);
}